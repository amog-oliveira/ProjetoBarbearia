    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // TODO add your handling code here:
        ClienteDAO clienteDAO = new ClienteDAO();
        Cliente cliente = new Cliente();
        
        cliente.setNome(inputNome.getText());
        cliente.setNascimento(inputNascimento.getText());
        cliente.setTelefone(inputTelefone.getText());
        cliente.setEndereco(inputEndereco.getText());
        cliente.setCpf(inputCpf.getText());
        cliente.setEmail(inputEmail.getText());
        
        Cliente clientAUX = new Cliente();
        clientAUX = clienteDAO.buscaCliente("cpf", cliente.getCpf());
        
        if(clientAUX.getCpf() != null){
            JOptionPane.showMessageDialog(null, "Não é possível cadastrar mais de um "
                    + "cliente com o mesmo cpf! "+clientAUX.getNome());
        }else{
            clienteDAO.inserir(cliente);
            JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!");
            inputCpf.setText("");
            inputNome.setText("");
            inputEndereco.setText("");
            inputTelefone.setText(""); 
            inputNascimento.setText("");
            inputEmail.setText("");
            
            labelNome.setEnabled(false);
            inputNome.setEnabled(false);
            labelEnderco.setEnabled(false);
            inputEndereco.setEnabled(false);
            labelTelefone.setEnabled(false);
            inputTelefone.setEnabled(false);
            labelNasc.setEnabled(false);
            inputNascimento.setEnabled(false);  
            btSalvar.setEnabled(false);
            labelEmail.setEnabled(false);
            inputEmail.setEnabled(false);

        }
        
    }//GEN-LAST:event_btSalvarActionPerformed

