    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        ClienteDAO clienteDAO = new ClienteDAO();
        Cliente cliente = new Cliente();
        
        cliente.setNome(inputNome.getText());
        cliente.setNascimento(inputNascimento.getText());
        cliente.setTelefone(inputTelefone.getText());
        cliente.setEndereco(inputEndereco.getText());
        cliente.setCpf(inputCpf.getText());
        cliente.setEmail(inputEmail.getText());
        
        String busca = "CPF";
        Cliente pesquisa = clienteDAO.buscaCliente(busca, cliente.getCpf());
        if(pesquisa.getCpf() != null){
            JOptionPane.showMessageDialog(null, busca+" " + pesquisa.getCpf()+ " já cadastrado para o(a) cliente de nome: " + pesquisa.getNome());
            labelNome.setEnabled(false);
            inputNome.setEnabled(false);
            labelEnderco.setEnabled(false);
            inputEndereco.setEnabled(false);
            labelTelefone.setEnabled(false);
            inputTelefone.setEnabled(false);
            labelNasc.setEnabled(false);
            inputNascimento.setEnabled(false);  
            btSalvar.setEnabled(false);
            labelEmail.setEnabled(false);
            inputEmail.setEnabled(false);
            
        } else {
            JOptionPane.showMessageDialog(null, busca+" ainda não cadastrado no banco de dados!");
            labelNome.setEnabled(true);
            inputNome.setEnabled(true);
            labelEnderco.setEnabled(true);
            inputEndereco.setEnabled(true);
            labelTelefone.setEnabled(true);
            inputTelefone.setEnabled(true);
            labelNasc.setEnabled(true);
            inputNascimento.setEnabled(true); 
            btSalvar.setEnabled(true);
            labelEmail.setEnabled(true);
            inputEmail.setEnabled(true);          
            
            
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

