    private void buttonCadastrarAgendamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCadastrarAgendamentoActionPerformed
        HorariosDAO dao = new HorariosDAO();
        Cliente cliente = (Cliente) jComboBoxClientes.getSelectedItem();
        Horarios horario = new Horarios();       
        
        
        horario.setDescricao(inputDescricao.getText());
        horario.setIdCliente(cliente.getIdCliente());
        horario.setDia(inputDia.getText());
        horario.setHorario(inputHorario.getText());
        
        dao.inserir(horario);
        
        JOptionPane.showMessageDialog(null, "Cliente agendado com sucesso!");
        inputDescricao.setText("");
        inputDia.setText("");
        inputHorario.setText("");
        inputDescricao.setEnabled(false);
        labelDesc.setEnabled(false);
        buttonCadastrarAgendamento.setEnabled(false);
        
    }//GEN-LAST:event_buttonCadastrarAgendamentoActionPerformed

