    private void buttonBuscarDiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBuscarDiaActionPerformed
        ArrayList<Horarios> listaHorario = new ArrayList<Horarios>();
        HorariosDAO dao = new HorariosDAO();
        listaHorario = dao.buscaHorarios(inputDia.getText(), inputHorario.getText());
        
        /*
        if (inputDia.getText().equals("") && inputHorario.getText().equals("")){            
            listaHorario = dao.buscaHorarios(inputDia.getText(), inputHorario.getText());
            JOptionPane.showMessageDialog(null," Insira o horario!");
        }
        */        
        
        if(listaHorario.size()>0){
            JOptionPane.showMessageDialog(null, "Horário Indisponível!");
            inputDescricao.setEnabled(false);
            labelDesc.setEnabled(false);
            buttonCadastrarAgendamento.setEnabled(false);
            
        }else{
            JOptionPane.showMessageDialog(null, "Horário Disponível!");
            inputDescricao.setEnabled(true);
            labelDesc.setEnabled(true);
            buttonCadastrarAgendamento.setEnabled(true);
        }

    }//GEN-LAST:event_buttonBuscarDiaActionPerformed

